#' Computes the sum.
#'
#' @param a
#' @param b
#' @param c
#' @param d
#' @param e
#' @export
#' 
sum <- function (a=0,b=0,c=0,d=0,e=0){
  a+b+c+d+e
}

#' Computes the product.
#'
#' @param a
#' @param b
#' @param c
#' @param d
#' @param e
#' @export
#' 
product <- function (a=1,b=1,c=1,d=1,e=1){
  a*b*c*d*e
}

#' Equality.
#'
#' @param a
#' @param b
#' @export
#' 
equals <- function (a,b){
  (a==b)*1
}